UI_TRANSLATIONS: dict[str, dict[str, str]] = {
    "ko": KO_TRANSLATIONS,
    "zh_cn": ZH_CN_TRANSLATIONS,
    "zh_tw": ZH_TW_TRANSLATIONS,
}


def translate_ui(text: str | None, lang: str = "en", **kwargs: Any) -> str:
    source = "" if text is None else str(text)
    ui_lang = normalize_ui_language(lang)
    translated = source if ui_lang == "en" else UI_TRANSLATIONS.get(ui_lang, {}).get(source, source)
    try:
        return translated.format(**kwargs) if kwargs else translated
    except Exception:
        return translated
